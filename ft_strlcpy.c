#include "libft.h"

size_t	ft_strlcpy(char *dst, const char *src, size_t size)
{
	unsigned long	src_size;
	unsigned long	i;

	if (!src)
		return (0);
	src_size = 0;
	i = 0;
	while (src[src_size])
		src_size++;
	if (!dst)
		return (src_size);
	if (size != 0)
	{
		while (src[i] && i < (size - 1))
		{
			dst[i] = src[i];
			i++;
		}
		dst[i] = '\0';
	}
	return (src_size);
}
